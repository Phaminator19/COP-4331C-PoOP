package com.example.project_1;

public class GroupInformation {
    private String GroupName;
    private String GroupInterest;
    private String GroupBios;

    public GroupInformation() {
    }

    public String getGroupName() {
        return GroupName;
    }

    public void setGroupName(String groupName) {
        GroupName = groupName;
    }

    public String getGroupBios() {
        return GroupBios;
    }

    public void setGroupBios(String groupBios) {
        GroupBios = groupBios;
    }

    public String getGroupInterest() {
        return GroupInterest;
    }

    public void setGroupInterest(String groupInterest) {
        GroupInterest = groupInterest;
    }

}
